// ─────────────────────────────────────────────────────────────
// 10. TROUBLESHOOTING
// ─────────────────────────────────────────────────────────────

/**
 * Q: I don't hear any sound!
 * A: 1. Check the status bar shows "Funny Sounds" with an unmute icon.
 *    2. Run "Funny Sounds: Test Sound" from the command palette.
 *    3. Make sure `sounds/error.wav` exists or a custom path is set.
 *    4. On Linux, ensure `paplay`, `aplay`, or `ffplay` is installed.
 *
 * Q: Sound plays too often!
 * A: Increase the cooldown:
 *    "funnySounds.cooldownMs": 5000
 *
 * Q: Sound doesn't play for my framework's errors!
 * A: Add the error text to "funnySounds.errorPatterns" in settings.
 *
 * Q: How do I completely uninstall?
 * A: Run: code --uninstall-extension funny-sounds
 *    Or find it in Extensions sidebar → Uninstall.
 */

