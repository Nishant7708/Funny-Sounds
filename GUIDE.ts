//  1. WHAT DOES THIS EXTENSION DO?
// ─────────────────────────────────────────────────────────────

/**
 * Funny Sounds listens to your VS Code terminal output in real-time.
 * Whenever a line containing an error keyword appears (e.g. "Error",
 * "FATAL", "SyntaxError"), it plays a sound so you *hear* the failure
 * instantly — even if the terminal is hidden or you're looking at
 * another tab.
 *
 * Features:
 *  - Plays a "boing" sound on terminal errors (default .wav included)
 *  - Use your own sound file (.wav, .mp3, .ogg)
 *  - Configurable error patterns, volume, and cooldown
 *  - Status bar toggle to quickly enable/disable
 *  - Works on Windows, macOS, and Linux
 */

