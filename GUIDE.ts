enum Commands {
  /** Enable error sound playback */
  Enable = "funnySounds.enable",

  /** Disable error sound playback */
  Disable = "funnySounds.disable",

  /** Play the current sound once (for testing) */
  TestSound = "funnySounds.testSound",

  /** Open a file picker to choose a custom sound file */
  SelectSound = "funnySounds.selectSound",
}

// ─────────────────────────────────────────────────────────────
//  5. EXTENSION SETTINGS
// ─────────────────────────────────────────────────────────────

interface FunnySoundsSettings {
  /**
   * Master switch.
   * @default true
   */
  "funnySounds.enabled": boolean;

  /**
   * Path to a custom sound file (.wav / .mp3 / .ogg).
   * Leave empty to use the built-in "boing" sound.
   * @default ""
   */
  "funnySounds.soundFilePath": string;

  /**
   * Volume level from 0 (silent) to 100 (full).
   * @default 50
   */
  "funnySounds.volume": number;

  /**
   * Strings to match in terminal output. If any pattern is found
   * in a terminal write event, the sound plays.
   * @default ["error","Error","ERROR","fatal","FATAL", ...]
   */
  "funnySounds.errorPatterns": string[];

  /**
   * Minimum milliseconds between consecutive sound plays.
   * Prevents rapid-fire audio when many errors print at once.
   * @default 2000
   */
  "funnySounds.cooldownMs": number;
}

// ─────────────────────────────────────────────────────────────
//  6. HOW THE TERMINAL LISTENER WORKS
// ─────────────────────────────────────────────────────────────

/**
 * The core mechanism uses the VS Code API:
 *
 *   vscode.window.onDidWriteTerminalData(event => { ... })
 *
 * This event fires every time text is written to any terminal.
 * The extension checks `event.data` against the configured
 * error patterns. If a match is found, it calls `SoundPlayer.play()`.
 *
 * The SoundPlayer uses platform-specific commands:
 *
 *   Windows : PowerShell System.Media.SoundPlayer
 *   macOS   : afplay
 *   Linux   : paplay / aplay / ffplay (first available)
 *
 * A cooldown timer prevents audio spam when errors cascade.
 */

// ─────────────────────────────────────────────────────────────
//  7. USING YOUR OWN SOUND
// ─────────────────────────────────────────────────────────────

/**
 * Option A — Via the command palette:
 *   1. Press Ctrl+Shift+P (or Cmd+Shift+P on macOS)
 *   2. Run "Funny Sounds: Select Custom Sound File"
 *   3. Pick any .wav, .mp3, or .ogg file
 *
 * Option B — Via settings.json:
 *   {
 *     "funnySounds.soundFilePath": "C:/Sounds/bruh.wav"
 *   }
 *
 * Option C — Replace the default sound:
 *   Overwrite `sounds/error.wav` with your own .wav file.
 */

// ─────────────────────────────────────────────────────────────
//  8. CUSTOMIZING ERROR PATTERNS
// ─────────────────────────────────────────────────────────────

/**
 * Add or remove patterns in your VS Code settings:
 *
 *   "funnySounds.errorPatterns": [
 *     "error",
 *     "Error",
 *     "FAILED",
 *     "panic",             // Rust / Go panics
 *     "Traceback",         // Python stack traces
 *     "COMPILATION FAILED" // Gradle builds
 *   ]
 *
 * Patterns are simple substring matches (not regex) for
 * maximum performance since terminal output can be very fast.
 */

